import * as React from "react"
import { Select as SelectPrimitive } from "radix-ui"
import { cn } from "@/lib/utils"
import { ChevronDownIcon, CheckIcon, ChevronUpIcon } from "lucide-react"

interface SelectOption {
    value: string | number;
    label: string;
}

interface SelectProps extends Omit<React.ComponentPropsWithoutRef<"select">, "value" | "onChange" | "dir"> {
    label?: string;
    error?: string;
    hint?: string;
    required?: boolean;
    options?: SelectOption[];
    placeholder?: string;
    value?: string | number;
    onChange?: (event: React.ChangeEvent<HTMLSelectElement>) => void;
    onValueChange?: (value: string) => void;
    dir?: "ltr" | "rtl";
}

// Overwrite Root Select with unified native/Radix selector
const Select = React.forwardRef<HTMLSelectElement, SelectProps>(function Select(
    { label, error, hint, required, options, placeholder, className = '', id, children, ...props },
    ref
) {
  // If options array or placeholder is provided, render native select wrapper to ensure backward compatibility
  if (options || placeholder) {
      const inputId = id ?? label?.toLowerCase().replace(/\s+/g, '-');
      const optList = options || [];
      return (
          <div className="form-group flex flex-col gap-1.5 w-full">
              {label && (
                  <label htmlFor={inputId} className={`form-label text-sm font-medium text-secondary-foreground ${required ? 'required' : ''}`}>
                      {label}
                      {required && <span className="text-destructive ml-1">*</span>}
                  </label>
              )}
              <select
                  ref={ref}
                  id={inputId}
                  className={cn(
                      "h-9 w-full rounded-lg border border-stone-200 dark:border-stone-850 bg-white dark:bg-stone-900 px-3 py-1.5 text-base transition-colors outline-none focus-visible:border-primary-500 focus-visible:ring-3 focus-visible:ring-primary-500/15 disabled:pointer-events-none disabled:cursor-not-allowed disabled:bg-input/50 disabled:opacity-50 aria-invalid:border-destructive aria-invalid:ring-3 aria-invalid:ring-destructive/20 md:text-sm shadow-sm",
                      error ? 'border-destructive ring-destructive/20' : '',
                      className
                  )}
                  aria-invalid={!!error}
                  {...props}
              >
                  {placeholder && (
                      <option value="" disabled>
                          {placeholder}
                      </option>
                  )}
                  {optList.map((opt: SelectOption) => (
                      <option key={opt.value} value={opt.value} className="dark:bg-zinc-900 dark:text-foreground">
                          {opt.label}
                      </option>
                  ))}
                  {children}
              </select>
              {error && <p className="form-error text-xs text-destructive mt-1" role="alert">{error}</p>}
              {hint && !error && <p className="form-hint text-xs text-muted-foreground mt-1">{hint}</p>}
          </div>
      );
  }

  // Fallback to Radix Select Root
  return <SelectPrimitive.Root data-slot="select" {...(props as any)} >{children}</SelectPrimitive.Root>
});

function SelectGroup({
  className,
  ...props
}: React.ComponentProps<typeof SelectPrimitive.Group>) {
  return (
    <SelectPrimitive.Group
      data-slot="select-group"
      className={cn("scroll-my-1 p-1", className)}
      {...props}
    />
  )
}

function SelectValue({
  ...props
}: React.ComponentProps<typeof SelectPrimitive.Value>) {
  return <SelectPrimitive.Value data-slot="select-value" {...props} />
}

function SelectTrigger({
  className,
  size = "default",
  children,
  ...props
}: React.ComponentProps<typeof SelectPrimitive.Trigger> & {
  size?: "sm" | "default"
}) {
  return (
    <SelectPrimitive.Trigger
      data-slot="select-trigger"
      data-size={size}
      className={cn(
        "flex w-fit items-center justify-between gap-1.5 rounded-lg border border-stone-200 dark:border-stone-850 bg-white dark:bg-stone-900 py-2 pr-2 pl-2.5 text-sm whitespace-nowrap transition-colors outline-none select-none focus-visible:border-primary-500 focus-visible:ring-3 focus-visible:ring-primary-500/15 disabled:cursor-not-allowed disabled:opacity-50 aria-invalid:border-destructive aria-invalid:ring-3 aria-invalid:ring-destructive/20 data-placeholder:text-muted-foreground data-[size=default]:h-8 data-[size=sm]:h-7 data-[size=sm]:rounded-[min(var(--radius-md),10px)] *:data-[slot=select-value]:line-clamp-1 *:data-[slot=select-value]:flex *:data-[slot=select-value]:items-center *:data-[slot=select-value]:gap-1.5 dark:hover:bg-stone-850/50 dark:aria-invalid:border-destructive/50 dark:aria-invalid:ring-destructive/40 [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4 shadow-sm",
        className
      )}
      {...props}
    >
      {children}
      <SelectPrimitive.Icon asChild>
        <ChevronDownIcon className="pointer-events-none size-4 text-muted-foreground" />
      </SelectPrimitive.Icon>
    </SelectPrimitive.Trigger>
  )
}

function SelectContent({
  className,
  children,
  position = "item-aligned",
  align = "center",
  ...props
}: React.ComponentProps<typeof SelectPrimitive.Content>) {
  return (
    <SelectPrimitive.Portal>
      <SelectPrimitive.Content
        data-slot="select-content"
        data-align-trigger={position === "item-aligned"}
        className={cn("relative z-50 max-h-(--radix-select-content-available-height) min-w-36 origin-(--radix-select-content-transform-origin) overflow-x-hidden overflow-y-auto rounded-lg bg-popover text-popover-foreground shadow-md ring-1 ring-foreground/10 duration-100 data-[align-trigger=true]:animate-none data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2 data-open:animate-in data-open:fade-in-0 data-open:zoom-in-95 data-closed:animate-out data-closed:fade-out-0 data-closed:zoom-out-95", position ==="popper"&&"data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1", className )}
        position={position}
        align={align}
        {...props}
      >
        <SelectScrollUpButton />
        <SelectPrimitive.Viewport
          data-position={position}
          className={cn(
            "data-[position=popper]:h-(--radix-select-trigger-height) data-[position=popper]:w-full data-[position=popper]:min-w-(--radix-select-trigger-width)",
            position === "popper" && ""
          )}
        >
          {children}
        </SelectPrimitive.Viewport>
        <SelectScrollDownButton />
      </SelectPrimitive.Content>
    </SelectPrimitive.Portal>
  )
}

